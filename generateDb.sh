rm pbp_2020.sqlite

sqlite3 pbp_2020.sqlite << 'END_SQL'
.mode csv

.import pbp_2020.csv plays_text

.schema plays_text

create table plays (
    game_id int,
    game_date text,
    quarter int,
    minute int,
    second int,
    offense_team text,
    defense_team text,
    down int,
    to_go int,
    yard_line int,
    series_first_down boolean,
    next_score boolean,
    description text,
    team_win boolean,
    season_year int,
    yards int,
    formation text,
    play_type text,
    is_rush boolean,
    is_pass boolean,
    is_incomplete boolean,
    is_touchdown boolean,
    pass_type text,
    is_sack boolean,
    is_challenge boolean,
    is_challenge_reversed boolean,
    challenger text,
    is_measurement boolean,
    is_interception boolean,
    is_fumble boolean,
    is_penalty boolean,
    is_two_point_conversion boolean,
    is_two_point_conversion_successful boolean,
    rush_direction text,
    yard_line_fixed int,
    yard_line_direction text,
    is_penalty_accepted boolean,
    penalty_team text,
    is_no_play boolean,
    penalty_type text,
    penalty_yards int
);

insert into plays(
    game_id,
    game_date,
    quarter,
    minute,
    second,
    offense_team,
    defense_team,
    down,
    to_go,
    yard_line,
    series_first_down,
    next_score,
    description,
    team_win,
    season_year,
    yards,
    formation,
    play_type,
    is_rush,
    is_pass,
    is_incomplete,
    is_touchdown,
    pass_type,
    is_sack,
    is_challenge,
    is_challenge_reversed,
    challenger,
    is_measurement,
    is_interception,
    is_fumble,
    is_penalty,
    is_two_point_conversion,
    is_two_point_conversion_successful,
    rush_direction,
    yard_line_fixed,
    yard_line_direction,
    is_penalty_accepted,
    penalty_team,
    is_no_play,
    penalty_type,
    penalty_yards
) 
select 
    "GameId", 
    "GameDate", 
    "Quarter", 
    "Minute", 
    "Second", 
    "OffenseTeam",
    "DefenseTeam", 
    "Down", 
    "ToGo", 
    "YardLine",
    "SeriesFirstDown",
    "NextScore",
    "Description",
    "TeamWin",
    "SeasonYear",
    "Yards",
    "Formation",
    "PlayType",
    "IsRush",
    "IsPass",
    "IsIncomplete",
    "IsTouchdown",
    "PassType",
    "IsSack",
    "IsChallenge",
    "IsChallengeReversed",
    "Challenger",
    "IsMeasurement",
    "IsInterception",
    "IsFumble",
    "IsPenalty",
    "IsTwoPointConversion",
    "IsTwoPointConversionSuccessful",
    "RushDirection",
    "YardLineFixed",
    "YardLineDirection",
    "IsPenaltyAccepted",
    "PenaltyTeam",
    "IsNoPlay",
    "PenaltyType",
    "PenaltyYards" 
from plays_text;

END_SQL
